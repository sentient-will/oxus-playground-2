import importlib.util
import sys
from pathlib import Path
from typing import Any, Callable, Dict

import yaml


def load_module_from_path(module_path: Path) -> Any:
    """Dynamically import a Python module from a file path.
    
    Args:
        module_path: Path to the Python file
        
    Returns:
        The imported module
    """
    module_name = module_path.stem
    spec = importlib.util.spec_from_file_location(module_name, module_path)
    if spec is None or spec.loader is None:
        raise ImportError(f"Could not load module from {module_path}")
    
    module = importlib.util.module_from_spec(spec)
    sys.modules[module_name] = module
    spec.loader.exec_module(module)
    return module


def get_handler_from_module(module: Any, handler_name: str = "handler") -> Callable:
    """Extract handler function from a module.
    
    Tries multiple strategies:
    1. Direct function: module.handler
    2. Default export: module.default
    3. Module-level function with name 'handler'
    
    Args:
        module: The imported module
        handler_name: Name of the handler function (default: "handler")
        
    Returns:
        The handler function
        
    Raises:
        AttributeError: If handler not found
    """
    # Try direct attribute
    if hasattr(module, handler_name):
        handler = getattr(module, handler_name)
        if callable(handler):
            return handler
    
    # Try default
    if hasattr(module, "default"):
        handler = getattr(module, "default")
        if callable(handler):
            return handler
    
    # Try module itself if it's callable
    if callable(module):
        return module
    
    raise AttributeError(f"Handler '{handler_name}' not found in module")


def load_capabilities_from_module_yaml(module_root: Path) -> Dict[str, Callable]:
    """Load all Python capabilities from module.yaml and their handlers.
    
    Args:
        module_root: Root directory of the module (where module.yaml is located)
        
    Returns:
        Dictionary mapping capability_id -> handler function
    """
    module_yaml_path = module_root / "module.yaml"
    if not module_yaml_path.exists():
        return {}
    
    with module_yaml_path.open() as f:
        module_config = yaml.safe_load(f)
    
    capabilities: Dict[str, Callable] = {}
    
    # Navigate through submodules and capabilities
    submodules = module_config.get("submodules", [])
    for submodule in submodules:
        submodule_capabilities = submodule.get("capabilities", [])
        for capability in submodule_capabilities:
            capability_id = capability.get("id")
            handler_path = capability.get("handler")
            languages = capability.get("languages", [])
            
            # Only process Python capabilities
            if capability_id and handler_path and "python" in languages:
                # Resolve handler path relative to module root
                handler_file = (module_root / handler_path).resolve()
                
                if handler_file.exists():
                    try:
                        # Load the module
                        module = load_module_from_path(handler_file)
                        # Extract handler function
                        handler = get_handler_from_module(module)
                        capabilities[capability_id] = handler
                    except Exception as e:
                        # Log error but continue with other capabilities
                        print(f"Warning: Failed to load handler for capability '{capability_id}': {e}")
                else:
                    print(f"Warning: Handler file not found: {handler_file}")
    
    return capabilities


